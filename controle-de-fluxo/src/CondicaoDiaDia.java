public class CondicaoDiaDia {
    public static void main(String[] args) {
        boolean paiAlimentouGato = false;

        if (paiAlimentouGato) {
            System.out.println(
                    "nao precisa alimentar o gato"
            );
        } else {
            System.out.println(
                    "precisa alimentar o gato"
            );
        }
    }
}
