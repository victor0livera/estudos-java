public class Luta {
    public static void main(String[] args) {
        System.out.println("Contagem regressiva para luta: ");

        // contagem regressiva para a luta!
        int zero = 0;
        for (int i = 10; i > zero ; i--) {
            System.out.println("Contagem: " + i);
        }
        System.out.println("Lute!!!");

    }
}